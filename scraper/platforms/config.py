def get_user_agent(platform: str) -> str:
    """Get user agent for a platform."""
    return PLATFORM_USER_AGENTS.get(platform, PLATFORM_USER_AGENTS["x"])


def get_headers(platform: str) -> dict:
    """Get default headers for a platform."""
    base_headers = PLATFORM_HEADERS.get(platform, {})
    return {
        **base_headers,
        "User-Agent": get_user_agent(platform),
    }


def get_timeout(platform: str) -> int:
    """Get timeout for a platform."""
    return PLATFORM_TIMEOUTS.get(platform, 30)


def get_retry_count(platform: str) -> int:
    """Get retry count for a platform."""
    return PLATFORM_RETRIES.get(platform, 3)
